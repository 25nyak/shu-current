// EnhancedStakingPlatform contract address
const contractAddress = '0x8c6c44182db04a82237abc14d7c2bbf1904dd67f'; // Replace with your actual contract address

// BSC WebSocket provider URL
const bscProviderUrl = 'wss://bsc-ws-node.nariox.org'; // Replace with your actual BSC WebSocket provider URL

// Check if Web3 is already defined by some other source (e.g., MetaMask)
let web3;

if (typeof window.ethereum !== 'undefined') {
    // Use the MetaMask provider
    web3 = new Web3(window.ethereum);
} else {
    // Fallback to BSC WebSocket provider
    web3 = new Web3(new Web3.providers.WebsocketProvider(bscProviderUrl));
}

// Contract instance
const enhancedStakingPlatformContract = new web3.eth.Contract(contractABI, contractAddress);

// Function to check the wallet and update UI
async function updateUI() {
    // Check if MetaMask is installed and connected
    if (window.ethereum) {
        try {
            // Request account access if needed
            await window.ethereum.enable();

            // Get the account address
            const accounts = await web3.eth.getAccounts();
            const accountAddress = accounts[0];

            // Display account address
            document.getElementById('accountAddress').innerText = accountAddress;

            // Get staked balance
            const stakedBalance = await enhancedStakingPlatformContract.methods.stakedBalances(accountAddress).call();
            document.getElementById('stakedBalance').innerText = stakedBalance;

            // Get rewards
            const rewards = await enhancedStakingPlatformContract.methods.calculateRewards().call();
            document.getElementById('rewards').innerText = rewards;
        } catch (error) {
            console.error(error);
        }
    } else {
        console.error('No wallet is installed');
    }
}

// Call the updateUI function or execute your code when needed
updateUI();
